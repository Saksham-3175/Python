#List methods 
#1. append() - Adds an element at the end of the list.
#2. perpend() - Adds an element at the start of the list.
#3. insert() - Adds an element at the specified position.
#4. clear() - Removes all the elements from the list.
#5. copy() - Returns a copy of the list.
#6. count() - Returns the number of elements with the specified value.
#7. extend() - Adds the elements of one list to another. Pretty much like concatenation or appending lists.
