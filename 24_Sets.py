#Sets in python
#Set is a unordered collection of data items.
#Every thing in python is a object.
#set is used when you don't want to repeat the data item in a collection.
#They are immutable.
#Example:
set1 = {1, 2, 4, 5, 2, 4}
print(set1)

#Empty sets
#The below code will return the type as dictionary and not set as both dictionary and sets are defined by curly braces.
empty = {}
print(type(empty))

#To define an empty set, use the below code:
empty1 = set()
print(type(empty1))
